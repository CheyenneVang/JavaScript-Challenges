// Addition function
function add(numOne, numTwo) {
    var addNum = numOne + numTwo; // use var because the values will change
    return addNum; // Return the calculation of addNum
}
  //add() is a function/ a set of code that is ran and it gives you a value
  // var x = add(2, 3); You could declare a variable and console log it
  console.log(add(1,10));
  
// Subtraction function
function sub(numOne, numTwo) {
    return numOne - numTwo;
}
console.log(sub(1,10));
  
// Multiplication function
function multi(numOne, numTwo) {
    return numOne * numTwo;
}
console.log(multi(2, 3));
  
// Division function
function div(numOne, numTwo) {
    return numOne / numTwo;
}
console.log(div(10, 5));